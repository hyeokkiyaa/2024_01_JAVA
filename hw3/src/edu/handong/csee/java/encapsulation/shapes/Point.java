package edu.handong.csee.java.encapsulation.shapes;

/**
 * setting point in this class
 */
public class Point {
    /**
     * x is rect's x point
     */
    public int x = 0;
    /**
     * y is rect's y point
     */
    public int y = 0;
    
    /**
     * this is a constructor to make points of the rectangle
     * @param a x point
     * @param b y point
     */
    public Point(int a, int b) {
        x = a;
        y = b;
    }
}

